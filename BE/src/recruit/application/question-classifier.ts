// ── 핵심 분류 함수 ──────────────────────────────────────────────────────────────

/**
 * 문항 제목에서 카테고리 태그를 추출합니다.
 *
 * @param title   문항 제목 (질문 텍스트)
 * @param answer  답변 텍스트 (선택적 — 제목에서 매칭 실패 시 fallback)
 * @returns       매칭된 태그 목록 (우선순위 순, 중복 없음)
 *
 * @example
 * classifyQuestionTitle('지원직무를 어떻게 이해하고 있는지...')
 * // → ['직무적합성']
 *
 * classifyQuestionTitle('공동의 목표를 달성하기 위해 힘을 합쳐...')
 * // → ['협업/팀워크']
 */
export function classifyQuestionTitle(
  title: string,
  answer?: string,
): QuestionTag[] {
  // 1단계: 제목(title)만으로 분류 — 높은 정확도
  const titleTags = QUESTION_CATEGORIES.filter((rule) =>
    rule.titlePatterns.some((p) => p.test(title)),
  ).map((rule) => rule.tag);

  if (titleTags.length > 0) return titleTags;

  // 2단계: 제목 + 답변 함께 분류 (fallback) — 관대한 매칭
  if (answer) {
    const fullText = `${title}\n${answer}`;
    return QUESTION_CATEGORIES.filter(
      (rule) =>
        rule.titlePatterns.some((p) => p.test(fullText)) ||
        rule.answerPatterns.some((p) => p.test(fullText)),
    ).map((rule) => rule.tag);
  }

  return [];
}

/**
 * 문항에서 검색용 키워드를 추출합니다.
 * (태그 + 제목/답변에서 의미있는 단어)
 */
export function extractQuestionKeywords(
  title: string,
  answer?: string,
  tags?: string[],
): string[] {
  const text = answer ? `${title}\n${answer}` : title;
  const stopwords = new Set([
    '그리고',
    '하지만',
    '입니다',
    '합니다',
    '있는',
    '없는',
    '제가',
    '저는',
    '이를',
    '통해',
    '대한',
    '위해',
    '에서',
    '으로',
    '하게',
    '되어',
    '하며',
    '또한',
    '그래서',
    '따라서',
    '때문에',
    '이후',
    '이전',
    '이때',
    '이런',
    '이런',
    '저런',
    '있어',
    '없어',
    '하여',
    '기술해',
    '기재해',
    '작성해',
    '입력가능',
    '최소',
    '최대',
  ]);
  const words = (text.match(/[가-힣A-Za-z0-9+#.]{2,}/g) ?? [])
    .map((w) => w.toLowerCase())
    .filter((w) => !stopwords.has(w));

  return [...new Set([...(tags ?? []), ...words])].slice(0, 80);
}
